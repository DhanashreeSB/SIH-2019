    public void loadNotifications()
    {
        progressDialog.setMessage("Please Wait");
        progressDialog.show();
        StringRequest stringRequest = new StringRequest(Request.Method.POST, NOTIFICATION_FETCH_URL, new Response.Listener<String>() {

            @Override
            public void onResponse(String response) {

                progressDialog.dismiss();
                try {
                    JSONArray jsonArray = new JSONArray(response);

                    for (int i=0; i<jsonArray.length();i++)
                    {
                        JSONObject jsonObject = jsonArray.getJSONObject(i);
                        String message = jsonObject.getString("notification");
                        Log.w("!!!!!!!!!!!!!!hooooooo",message);
                       NotificationDisplay notificationDisplay_local = new NotificationDisplay(1,message);
                       notificationDisplays.add(notificationDisplay_local);
                    }


                        //creating recyclerview adapter
                        NotificationDisplayAdapter adapter = new NotificationDisplayAdapter(getActivity(),notificationDisplays);

                        //setting adapter to recyclerview
                        recyclerView.setAdapter(adapter);

                } catch (JSONException e) {
                    e.printStackTrace();
                }

            }

        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                Toast.makeText(getActivity(),error.getMessage(),Toast.LENGTH_SHORT).show();
                progressDialog.dismiss();
            }
        });

        RequestQueue requestQueue = Volley.newRequestQueue(getActivity());
        // Adding the StringRequest object into requestQueue.
        requestQueue.add(stringRequest);


    }
